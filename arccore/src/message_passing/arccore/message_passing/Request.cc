Request::_Request Request::null_request;
int Request::null_request_type = Request::T_Null;

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void Request::
print(std::ostream& o) const
{
  if (m_type==T_Null)
    o << "(null)";
  else if (m_type==T_Int)
    o << m_request.i;
  else if (m_type==T_Long)
    o << m_request.l;
  else
    o << m_request.cv;
}

