void MessagePassing::PointToPointInfo::
print(std::ostream& o) const
{
  if (m_type==Type::T_Null)
    o << "(null)";
  else if (m_type==T_RankTag)
    o << "(rank=" << m_rank << ",tag=" << tag << ")";
  else
    o << m_message_id;
}

