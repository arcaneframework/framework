#include "arccore/base/Observable.h"
#include "arccore/base/Observer.h"

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

namespace Arcane
{

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

Observable::
~Observable()
{
  if (arccoreIsCheck()) {
    if (!m_observers.empty())
      std::cout << "** WARNING: Observable p=" << this
                << " is destroyed but has n="
                << m_observers.size() << " observer(s) attached\n";
  }
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void Observable::
attachObserver(IObserver* obs)
{
  // Vérifie que l'observeur n'est pas dans la liste.
  if (m_observers.contains(obs))
    return;
  obs->attachToObservable(this);
  m_observers.add(obs);
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void Observable::
detachObserver(IObserver* obs)
{
  //std::cout << "DETACH OB this=" << this << " o=" << obs
  //          << " destroyed=" << m_is_destroyed << '\n';
  if (m_is_destroyed)
    return;
  m_observers.removeValue(obs);
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void Observable::
notifyAllObservers()
{
  if (m_observers.empty())
    return;
  for( IObserver* o : m_observers)
    o->observerUpdate(this);
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

bool Observable::
hasObservers() const
{
  return (!m_observers.empty());
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void Observable::
_detachAllObservers()
{
  //std::cout << "DESTROY this=" << this << '\n';
  for (IObserver* o : m_observers) {
    o->detach();
  }
  m_observers.clear();
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void Observable::
detachAllObservers()
{
  _detachAllObservers();
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

IObservable* IObservable::
createDefault()
{
  return new Observable();
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

AutoDetachObservable::
~AutoDetachObservable()
{
  _detachAllObservers();
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

} // End namespace Arcane

