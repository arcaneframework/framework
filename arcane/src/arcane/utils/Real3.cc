std::istream& Real3::
assign(std::istream& i)
{
  i >> x >> ws >> y >> ws >> z;
  return i;
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

std::ostream& Real3::
print(std::ostream& o) const
{
  o << x << ' ' << y << ' ' << z;
  return o;
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

std::ostream& Real3::
printXyz(std::ostream& o) const
{
  o << '(' << x << ',' << y << ',' << z << ')';
 return o;
}

