void MD5HashAlgorithm::
computeHash(ByteConstArrayView input,ByteArray& output)
{
  unsigned char buf[16];
  _md5_buffer((const char*)input.data(),input.size(),buf);

  for( int i=0; i<16; ++i ){
    output.add(buf[i]);
  }
}

